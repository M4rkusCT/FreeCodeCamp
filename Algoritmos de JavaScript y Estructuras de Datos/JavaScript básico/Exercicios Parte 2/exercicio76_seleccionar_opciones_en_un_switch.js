// Seleccionando entre muchas opciones con declaración switch

function caseInSwitch(val) {
    let answer = "";
    // Cambia solo el código debajo de esta línea
    switch (val) {
        case 1:
            return answer = "alpha";
            break;
        case 2:
            return answer = "beta";
            break;
        case 3:
            return answer = "gamma";
            break;
        case 4:
            return answer = "delta";
            break;
    }    
        
    // Cambia solo el código encima de esta línea
    return answer;
}
  
caseInSwitch(1);